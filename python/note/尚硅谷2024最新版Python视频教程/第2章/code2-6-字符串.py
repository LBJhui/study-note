# 创建字符串
str1 = 'hello'
print(str1)

str2 = "hello"
print(str2)

str3 = '''2024
hello
world'''
print(str3)

str4 = "It's a hat"
print(str4)

str5 = 'It\'s a hat'
print(str5)

# 字符串拼接
# 注意: 字符串与数字不能相加
print("---------字符串拼接----------")
print(str1 + str4)

# 字符串乘法
print("---------字符串乘法----------")
print(str1 * 3)
